const COL1 = [DbIcon, ChartIcon, PinIcon, DocIcon, BarIcon, TableIcon, UsersIcon];
const COL2 = [UsersIcon, PhoneIcon, MailIcon, DownloadIcon, FilterIcon, SearchIcon, DbIcon];
const COL3 = [TableIcon, BarIcon, ChartIcon, DocIcon, PhoneIcon, PinIcon, MailIcon];

export function FloatingIconsBg() {
  const col1Ref = useRef<HTMLDivElement>(null);
  const col2Ref = useRef<HTMLDivElement>(null);
  const col3Ref = useRef<HTMLDivElement>(null);

  useEffect(() => {
    const onScroll = () => {
      const y = window.scrollY;
      if (col1Ref.current) {
        col1Ref.current.style.transform = `translateY(${y * 0.14}px)`;
      }
      if (col2Ref.current) {
        col2Ref.current.style.transform = `translateY(${y * 0.23}px)`;
      }
      if (col3Ref.current) {
        col3Ref.current.style.transform = `translateY(${y * 0.08}px)`;
      }
    };
    window.addEventListener("scroll", onScroll, { passive: true });
    return () => window.removeEventListener("scroll", onScroll);
  }, []);

  return (
    <div className="float-icon-bg" aria-hidden="true">
      <div ref={col1Ref} className="float-col float-col-1">
        {COL1.map((Icon, i) => (
          <div key={i} className="float-icon">
            <Icon />
          </div>
        ))}
      </div>
      <div ref={col2Ref} className="float-col float-col-2">
        {COL2.map((Icon, i) => (
          <div key={i} className="float-icon float-icon-sm">
            <Icon />
          </div>
        ))}
      </div>
      <div ref={col3Ref} className="float-col float-col-3">
        {COL3.map((Icon, i) => (
          <div key={i} className="float-icon float-icon-xs">
            <Icon />
          </div>
        ))}
      </div>
    </div>
  );
}
